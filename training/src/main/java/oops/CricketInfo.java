package oops;

class CricketInfo{

    private String format;

    private int numberOfOvers;

    public String getFormat(){
        return format;
    }

    public int getOvers(){
        return numberOfOvers;
    }

    public void setFormat(String format){
        this.format = format;
    }

    public void setOvers(int overs){
        numberOfOvers = overs;
    }

}
